async function loginUser(credentials) {
  let req_config = {
    headers: {
      'Content-Type': 'application/json'
    },
    withCredentials: true
  }

  return axios.post(`${process.env.REACT_APP_END_POINT}auth/login`, { username: credentials.username, password: credentials.password }, req_config)
  .then(res => {
    return res.data.data.token;
  })
}

